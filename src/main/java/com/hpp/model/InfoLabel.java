package com.hpp.model;

public class InfoLabel {
    private Integer id;

    private Integer fileId;

    private Integer labelListId;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getFileId() {
        return fileId;
    }

    public void setFileId(Integer fileId) {
        this.fileId = fileId;
    }

    public Integer getLabelListId() {
        return labelListId;
    }

    public void setLabelListId(Integer labelListId) {
        this.labelListId = labelListId;
    }
}